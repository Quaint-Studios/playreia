	<h2>Technical Design</h2>
	<blockquote>
		This section outlines the technical aspects of the game's development, including the game
		engine, graphics rendering, scripting languages, web programming, data handling, and
		specifications and scope.
	</blockquote>

	<h3>Game Programming</h3>
	<ol>
		<li>Game Engine: The Godot Game Engine (v4.5+).</li>
		<li>
			Graphics Rendering: Vulkan & Direct3D 12 support for both high-performance graphics and old
			GPUs. OpenGL ES 3.0 support web (HTML5) platforms and all companion releases.
		</li>
		<li>
			Programming Languages: GDScript for high-level programming, Rust through gdext for more
			performance.
		</li>
		<li>Shader Languages: GDShader/GLSL.</li>
		<li>Localization: Godot's built-in translation system.</li>
		<li>Networking: Sustenet, our custom networking solution built in Rust.</li>
	</ol>

	<h3>Web Programming</h3>
	<ol>
		<li>
			Frontend: SvelteKit & Tailwind CSS for optimizations and SEO. Paraglide for i18n translations.
		</li>
		<li>
			Backend: Vercel with Supabase for user authentication and storage. It'll also link with Turso
			to load specific user data.
		</li>
	</ol>

	<h3>Data Handling</h3>
	<ol>
		<li>
			Authentication: Turso through Rust handles all OpenID Connect (OIDC) auths for Steam / Epic
			Games / other platforms and also email / password signups. This gets linked to a Supabase
			account to be used on the website.
		</li>
		<li>Online Database: Turso with Rust for authentication and core game data.</li>
		<li>Offline Support: Turso with Rust enables seamless online and offline play.</li>
	</ol>

	<h3>Specs & Scope</h3>
	<ol>
		<li>
			Target Platforms: PC, Mobile support, and a heavy focus on Console ports later. The companion
			app will be a trimmed down 2D version of the game with a focus on crafting, gathering, and
			social features. It'll also have combat, but it'll be more casual and less intensive than the
			main game. This app is available on all platforms, including web (HTML5).
		</li>
		<li>Team Size: Core Team and community contributors.</li>
		<li>
			Project Timeline: We're aiming for an alpha release around May 2026. If it takes longer, we'll
			postpone to reach our goal properly for the following year. But we want the release to be in
			May for a Kickstarter.
		</li>
