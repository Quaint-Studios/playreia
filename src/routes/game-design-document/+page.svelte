	<h3>Target & Genre</h3>
	<ol>
		<li>Genre: Action Adventure RPG (MMORPG)</li>
		<li>
			Target Audience: PC gamers who enjoy action RPGs with online multiplayer elements, open world
			exploration, and deep character customization. Mobile and console players will also be a
			secondary target audience. And cozy gamers who enjoy crafting, gathering, and building their
			own spaces will be a tertiary target audience.
		</li>
		<li>
			Unique Selling Points:
			<ul>
				<li>Open-source development model allowing for community contribution.</li>
				<li>Deep character customization with spirits, soulstones, and skill trees.</li>
				<li>Player-owned islands and a massive, ever-expanding infinite city.</li>
				<li>Crafting and gathering to create potions, food, gear, spellstones, and more.</li>
			</ul>
		</li>
		<li>
			Overall Vision and Goals:
			<ul>
				<li>
					Create a vibrant and engaging online world that fosters community and player creativity.
				</li>
				<li>
					Offer deep and rewarding character progression that keeps players engaged for long play
					sessions.
				</li>
				<li>
					Foster a collaborative development environment where the community can contribute to the
					game's growth.
				</li>
			</ul>
		</li>
	</ol>

	<h3>Gameplay</h3>
	<ol>
		<li>
			Core Gameplay Loop: Explore the world, complete quests, battle enemies, collect resources,
			craft items, progress your character, and socialize with other players.
		</li>
		<li>
			Objectives: Complete quests, defeat challenging enemies, explore dungeons, and uncover the
			lore of the Reia's world.
		</li>
		<li>
			Progression: Gain experience by playing the game naturally to level up your character / items,
			unlock new skills and abilities with mastery over time included, and acquire powerful
			equipment.
		</li>
	</ol>

