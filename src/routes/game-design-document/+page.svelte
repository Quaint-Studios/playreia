	</blockquote>

	<ol>
		<li>
			<b>Name of Project:</b>
			Reia
		</li>
		<li>
			<b>Purpose of the Document:</b>
			This document outlines the design of Reia, an open-source action adventure RPG. It details the
			game's concept, mechanics, world, story, technical aspects, production plan, and more. This document
			serves as a guide for development and a reference point for anyone interested in the game's design.
		</li>
		<li>
			<b>Concept Statement:</b>
			Reia is an open-source action adventure RPG where players explore a vast online world, complete
			quests, battle using spirits and power-enhancing soulstones, create their own player-owned islands,
			explore a massive infinite city, and more.
		</li>
		<li>
			<b>Target Audience:</b>
			People who enjoy action RPGs with online multiplayer elements, open world exploration, and deep
			character customization. The game is designed to be accessible to both casual and hardcore gamers,
			with a focus on community involvement and creativity.
		</li>
	</ol>

	<h2>Game Concept Design</h2>
	<blockquote>
		This section details the genre, target audience, unique selling points, overall vision, and core
