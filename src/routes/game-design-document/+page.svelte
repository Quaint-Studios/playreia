	<h4>Reia Character Design Notes</h4>

	<p>
		<i>Reia is the main character of the game. She's one of the four recently created Ethereals.</i>
	</p>

	<p>
		<b>Personality and Traits:</b>
		Curious, brave, and empathetic. Reia has a strong sense of justice and a desire to help others. She
		is also highly intelligent and resourceful. She's kind and compassionate to those who live the world
		she created.
	</p>

	<p>
		<b>Physical Appearance:</b>
		Reia has a toned build with long, braided hair that glows with a soft, ethereal light. She has bright,
		expressive eyes and a warm smile. Her clothing is practical yet stylish, designed for adventure.
		She has a pendant on her sash near her hip. Inside of it is a star representing her symbol. This
		same symbol appears 3 times on her necklace and once at the end of her hair braid.
		<br />
		Her outfit is a mid-length dress with a high slit on one side for mobility. The dress is adorned
		with gold clips on the sides and she has a light shawl draped over her shoulders. Her dress also
		curves and tapers off at the ends.
		<br />
		She wears wedges that lace up to her calves. The wedges have a slight heel for style but are still
		practical. On the back of the wedges is her symbol along with some extra designs. The overall color
		palette is white with a subtle mix of all of the elements (fire, water, earth, air, light, dark)
		with an ombr√© design. And gold accents for jewelry.
	</p>

